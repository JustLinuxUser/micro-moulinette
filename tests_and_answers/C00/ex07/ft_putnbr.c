#include <unistd.h>

void	ft_putnbr(int n)
{
	long	nl;
	char	c;

	nl = n;
	if (nl < 0)
	{
		nl *= -1;
		write(1, "-", 1);
	}
	if (nl > 9)
		ft_putnbr(nl / 10);
	c = '0' + nl % 10;
	write(1, &c, 1);
}
